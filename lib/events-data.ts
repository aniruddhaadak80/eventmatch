// Helper functions
export function getEventsByCategory(category: string): Event[] {
    if (category === 'all') return sampleEvents;
    return sampleEvents.filter(event => event.category === category);
}

export function searchEvents(query: string): Event[] {
    const lowerQuery = query.toLowerCase();
    return sampleEvents.filter(event =>
        event.title.toLowerCase().includes(lowerQuery) ||
        event.description.toLowerCase().includes(lowerQuery) ||
        event.category.toLowerCase().includes(lowerQuery) ||
        event.city.toLowerCase().includes(lowerQuery) ||
        event.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
    );
}

export function getFeaturedEvents(): Event[] {
    return sampleEvents.filter(event => event.featured);
}

export function getFreeEvents(): Event[] {
    return sampleEvents.filter(event => event.price === 0);
}

export function getEventsByCity(city: string): Event[] {
    return sampleEvents.filter(event =>
        event.city.toLowerCase() === city.toLowerCase()
    );
}

export function getUpcomingEvents(limit: number = 10): Event[] {
    const today = new Date().toISOString().split('T')[0];
    return sampleEvents
        .filter(event => event.date >= today)
        .sort((a, b) => a.date.localeCompare(b.date))
        .slice(0, limit);
}
